/**
 *
 *  Assignment Operators
 *
 *    a+=b	   a = a + b
 *    a-=b	   a = a - b
 *    a*=b     a = a * b
 *	  a/=b	   a = a / b
 *	  a%=b	   a = a % b
 *
 *
 *
 */
fun main(){
    var firstnumber = 13

    var secondNumber = 6

    // +
    firstnumber+=secondNumber

    // -
    firstnumber-=secondNumber

    // *
    firstnumber*=secondNumber

    // /
    firstnumber/=secondNumber

    // %
    firstnumber%=secondNumber

    print(firstnumber)
}